public class CBBAtividadePratica2 {
	public static void main(String...args){
		//declarando e inicializando os três numeros
		int num1 = 10;
		int num2 = 15;
		int num3 = 9;

		//calculando a  soma e a média
		int soma = num1 + num2 + num3;
		float media = soma / 3f;

		//mostrando os resultados para o usuário
		System.out.println("N1: " + num1 + " | N2: " + num2 + " | N3: " +  num3);
		System.out.println("Soma dos números: " + soma);
		System.out.println("Média dos números: " + media);
	}
}
